          <h3>Prevenção em Saúde Bucal</h3> <br>
          <h5>A prevenção é um trabalho em equipe que envolve paciente e 
            dentistas, visando preservar a dentição natural, prevenindo o
             aparecimento e o progresso de doenças bucais. O processo começa 
             em casa, com uma boa higiene oral e uma dieta balanceada e
              continua com visitas regulares ao dentista para realização de
               exames, limpeza, radiografias e 
            aplicação de selantes e flúor para manter os dentes protegidos.</h5>   <br>
            <h3>Dentística</h3><br>
          <h5>Cada vez mais as pessoas buscam saúde, beleza física e estética. Nos dentes isso não é diferente. 
            Dentística é o ramo da Odontologia que atua na área da cosmética e da restauração dental. Essa especialidade 
            estuda e aplica de forma integrada o conjunto de procedimentos odontológicos com o objetivo de preservar e 
            devolver ao dente sua integridade estrutural, funcional e estética. Os profissionais 
            dessa área tratam de clareamentos dentais, uso de resinas diretas, peeling gengival, facetas e restaurações.</h5>
            <h3>Periodontia</h3><br>
            <h5>Periodontia é o tratamento que visa a cura de processos inflamatórios e infecciosos da gengiva.
                A gengiva é o tecido que reveste o osso que sustenta os dentes. Em virtude da má higiene, do fumo, do stress, 
                da baixa imunidade e de maus hábitos alimentares, a gengiva fica fragilizada às infecções decorrentes 
                de bactérias.</h5><br>
            <h3>Endodontia</h3>   <br> 
            <h5>A endodontia é o ramo da Odontologia que trata das lesões e doenças da polpa (nervo) e da raiz do dente.
               Popularmente, endodontia também é chamada de tratamento de canal. O tratamento consiste na remoção do 
               tecido mole (polpa) que se encontra na parte interna (canal) do dente e pode estar vivo, sadio, inflamado,
                infectado ou necrosado (morto).Depois de se remover esse tecido, o dentista esteriliza o canal
                 preenchendo-o com obturação em material específico.</h5><br>
